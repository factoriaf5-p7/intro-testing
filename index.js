export const suma = (numA, numB) => {
    return (numA + numB)
}

export const resta = (numA, numB) => {
    return (numA - numB)
}

export const multiplicacion = (numA, numB) => {
    return (numA * numB)
}

export const division = (numA, numB) => {
    return (numA / numB)
}

export const modulo = (numA, numB) => {
    return (numA % numB)
}